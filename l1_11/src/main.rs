fn main() {
    let t: Vec<f64> = vec!(-25.4, -27.0, 13.0, 19.0, 15.5, 24.5, -21.0, 32.5);
    //найдем максимальный и минимальный элемент, чтобы сформировать границы интервала
    let mut max = -2147483648.0;
    let mut min = 2147483647..0;
    for i in t.iter() {
        if t[i] > max {
            max = t[i];
        }
        if t[i] < min {
            min = t[i];
        }
    }
    // поделим на интервалы

}
